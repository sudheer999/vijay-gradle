package com.dikshatech.beans;

public class LeaveDetails {

	private String	leaveAccumalated;
	private String	leaveBalance;
	private String	leavesTaken;
	private String	leaveTypeName;
	private String	compOff;

	public String getLeaveAccumalated() {
		return leaveAccumalated;
	}

	public void setLeaveAccumalated(String leaveAccumalated) {
		this.leaveAccumalated = leaveAccumalated;
	}

	public String getLeaveBalance() {
		return leaveBalance;
	}

	public void setLeaveBalance(String leaveBalance) {
		this.leaveBalance = leaveBalance;
	}

	public String getLeavesTaken() {
		return leavesTaken;
	}

	public void setLeavesTaken(String leavesTaken) {
		this.leavesTaken = leavesTaken;
	}

	public String getLeaveTypeName() {
		return leaveTypeName;
	}

	public void setLeaveTypeName(String leaveTypeName) {
		this.leaveTypeName = leaveTypeName;
	}

	public String getCompOff() {
		return compOff;
	}

	public void setCompOff(String compOff) {
		this.compOff = compOff;
	}
}
